package ifelse;

public class CheckElseIf {
	
	public static void main(String[] args) {
		int r= 50;
		int s= 100;
		int t= 150;
		if (r==0) {
			System.out.println("If will print");
		}
		else if (s==0) {
			System.out.println("Else If 1 will print");
		}
		
		else if (t==150) {
			System.out.println("Else If 2 will print");
		}
		else {
			System.out.println("Else will print");
		}
	}

}
